package com.company.day028.etc;

public class Car006 {
	public String color;
	public String gear;
	public int door;

	public Car006() {
	}

	public Car006(String color, String gear, int door) {
		super();
		this.color = color;
		this.gear = gear;
		this.door = door;
	}

	@Override
	public String toString() {
		return "Car006 [color=" + color + ", gear=" + gear + ", door=" + door + "]";
	}

}
